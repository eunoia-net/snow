# Useful math functions and tips for variables and floats 

import math 

pi = 3.14 
x = 1 
y = 2 
z = 3

print(round(pi)) #Will round integer to closest whole integer
print(math.ceil(pi)) #Will round to the closest higher integer
print(math.floor(pi)) #Will round to closest lower integer
print(abs(pi)) #Will tell the absolute value of the integer
print(pow(pi),2) #Will exponentiate to the power
print(math.sqrt(pi)) #Will find the square root of the number 
print(max(x,y,z)) #Will find the largest integer value among the given set of integers
print(min(x,y,z)) #Will find the lowest integer value among the given set of integers 


